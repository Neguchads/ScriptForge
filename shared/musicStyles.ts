export type StyleMixerInput = {
  selectedGenres: string[];
  selectedSubGenres: string[];
  selectedCharacteristics: Partial<Record<keyof typeof MUSICAL_CHARACTERISTICS, string[]>>;
  selectedInfluences: string[];
  selectedEras: string[];
  selectedVocalStyles: string[];
  selectedProductionTechniques: string[];
  customName?: string;
};

export function generateStyleDescription(input: StyleMixerInput): string {
  const parts: string[] = [];

  if (input.selectedGenres.length > 0) {
    parts.push(`Genres: ${input.selectedGenres.join(", ")}`);
  }

  if (input.selectedSubGenres.length > 0) {
    parts.push(`Sub-genres: ${input.selectedSubGenres.join(", ")}`);
  }

  if (input.selectedInfluences.length > 0) {
    parts.push(`Influences: ${input.selectedInfluences.join(", ")}`);
  }

  if (input.selectedEras.length > 0) {
    parts.push(`Eras: ${input.selectedEras.join(", ")}`);
  }

  const charParts: string[] = [];
  for (const [key, values] of Object.entries(input.selectedCharacteristics)) {
    if (values && values.length > 0) {
      charParts.push(`${key}: ${values.join(", ")}`);
    }
  }
  if (charParts.length > 0) {
    parts.push(charParts.join("; "));
  }

  if (input.selectedVocalStyles.length > 0) {
    parts.push(`Vocal Styles: ${input.selectedVocalStyles.join(", ")}`);
  }

  if (input.selectedProductionTechniques.length > 0) {
    parts.push(`Production: ${input.selectedProductionTechniques.join(", ")}`);
  }

  return parts.join(" | ");
}

export function generateStylePrompt(input: StyleMixerInput): string {
  const description = generateStyleDescription(input);
  return `Create a Suno AI style prompt for: ${description}. Make it cohesive and professional.`;
}
